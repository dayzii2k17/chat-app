    public Thread esperarCon(final ServerSocket servidor)
    {
        Thread t  = new Thread()
        {
            public void run()
            {
                while(esperandoCon) 
                {
                    try
                    {
                        solicitacaoDeConexao sd = new solicitacaoDeConexao(servidor.accept());
                        th.add(sd);
                        sd.start();
                    }catch (IOException ex)
                    {
                        ex.printStackTrace();
                    }
                }
            }
        };
        return t;
    }
