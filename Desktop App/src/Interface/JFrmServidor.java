    private class solicitacaoDeConexao extends Thread
    {
        private String nome;
        private Socket socket;
        private BufferedReader entrada;
        private PrintWriter saida;
        private boolean alive = true;
        private solicitacaoDeConexao(Socket s)
        {
            socket = s;
        }
        @Override
        public void run()
        {
            try
            {
                entrada = new BufferedReader(new InputStreamReader( socket.getInputStream(), StandardCharsets.UTF_8));
    		saida = new PrintWriter(new OutputStreamWriter(socket.getOutputStream(), StandardCharsets.UTF_8), true);
                while(alive)
                {
                    saida.println("enter_username:");
                    nome = entrada.readLine();
                    if(nome == null)
                    {
                        return;
                    }
                    if(!nomes.contains(nome))
                    {
                        nomes.add(nome);
                        break;
                    }
                }
                saida.println("accept_username:");
                saidas.add(saida);
                for(PrintWriter writer : saidas) 
                {
                    writer.println("new_user:"+nomes);
                }
                saida.println("nome_sala:"+jTextField1.getText());
                while(alive) 
                {
                    String menssagem = entrada.readLine();
                    if(menssagem == null) 
                    {
                        return;
                    }
                    if(menssagem.equals("@<out_user>"))
                    {
                        break;
                    }
                    List<String> l = new ArrayList<>();
                    l.add(nome);
                    l.add(menssagem);
                    for(PrintWriter writer : saidas) 
                    {
                        writer.println("send_message:"+l);
                    }
                }
            }catch (IOException e) 
            {
                e.printStackTrace();
            }finally 
            {
                if(nome != null) 
                {
                    nomes.remove(nome);;
                }
                if(saida != null) 
                {
                    saidas.remove(saida);
                }
                try 
                {
                    socket.close();
                }catch (IOException e) 
                {
                   e.printStackTrace();
                }
                for(PrintWriter writer : saidas) 
                {
                    writer.println("out_user:"+nome);
                }
            }
        }
        public void kill()
        {
            List<String> l = new ArrayList<>();
            l.add("~Clara");
            l.add("A casa caiu.");
            saida.println("send_message:"+l);
            alive = false;
            try
            {
                socket.close();
            }catch(Exception e)
            {
                e.printStackTrace();
            }
            
        }
    }
