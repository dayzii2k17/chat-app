    private void jBtnOKActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_jBtnOKActionPerformed
    {//GEN-HEADEREND:event_jBtnOKActionPerformed
        // TODO add your handling code here:
        int quantos = 0;
        if(!jTxtIP.getText().isEmpty() && !jTxtPorta.getText().isEmpty())
        {
            ip = jTxtIP.getText();
            for(int i = 0; i < ip.length(); i++)
            {
                if(ip.charAt(i) == '.')
                {
                    quantos++;
                }
            }
            if(quantos == 3)
            { 
                porta = Integer.parseInt(jTxtPorta.getText());
                setVisible(false);
            }
        }
    }//GEN-LAST:event_jBtnOKActionPerformed

