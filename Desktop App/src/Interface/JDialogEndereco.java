    private void jTxtIPKeyTyped(java.awt.event.KeyEvent evt)//GEN-FIRST:event_jTxtIPKeyTyped
    {//GEN-HEADEREND:event_jTxtIPKeyTyped
        // TODO add your handling code here:
        String caractere = Character.toString(evt.getKeyChar());
        if(caractere.matches("[a-zA-Z]"))
        {
            evt.consume();
        }
        if(firstClick)
        {
            jTxtIP.setText("");
            jTxtIP.setForeground(Color.BLACK);
            jTxtIP.setFont(new java.awt.Font("Segoe UI", 0, 16));
            firstClick = false;
        }
    }//GEN-LAST:event_jTxtIPKeyTyped

    private void jTxtPortaKeyTyped(java.awt.event.KeyEvent evt)//GEN-FIRST:event_jTxtPortaKeyTyped
    {//GEN-HEADEREND:event_jTxtPortaKeyTyped
        // TODO add your handling code here:
        String caractere = Character.toString(evt.getKeyChar());
        if(!caractere.matches("[0-9]"))
        {
            evt.consume();
        }
        if(firstClick2)
        {
            jTxtPorta.setText("");
            jTxtPorta.setForeground(Color.BLACK);
            jTxtPorta.setFont(new java.awt.Font("Segoe UI", 0, 16));
            firstClick2 = false;
        }
        
    }//GEN-LAST:event_jTxtPortaKeyTyped
    
